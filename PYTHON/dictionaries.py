#   crear un diccionario en donde se le asigne un valor a cada llave, ejmp: Harry - gryffindor y Draco - Slydering


houses = {"Harry":"Griffindor", "Draco":"Slydering"}

# ahora si quiero saber en que casa esta Harry, realizo lo siguiente:

print (houses["Harry"])


# si quiero agregar una llave con un valor podria hacerlo de la siguiente forma:

houses ["Hermione"] = "Griffindor"

# aqui imprimimos ahora el valor asignado a la llave Hermione

print(houses["Hermione"])


